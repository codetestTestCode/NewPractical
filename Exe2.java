public class Exe2 {
    public static void main(String[] args) {
        // for odd Number   (Stat =1 ; add= 2)
       for(int x =1;x<=100;x+=2){
           System.out.print(x+":");
       }
        System.out.println();
       // for even Number   (Stat =0 ; add =2)
       int i = 0 ;
        while (i<=100){
            System.out.print(i+":");
            i+= 2;
        }
    }
}


